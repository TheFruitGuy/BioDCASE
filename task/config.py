UNLABELED_DATA_DIR = Path("./data_unlabeled/combined")


# ======================================================================
# Dataset splits
# ======================================================================
# Partitioning follows Table 1 of the paper. The eight training sites and
# three validation sites are fixed; the two evaluation sites (Kerguelen2020,
