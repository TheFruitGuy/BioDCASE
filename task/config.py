"""
Whale-VAD Reproduction - Configuration Module
=============================================

Centralized configuration for all hyperparameters, paths, and constants used
across the Whale-VAD reproduction pipeline. All values are chosen to match the
paper (Geldenhuys et al., DCASE 2025) and the authors' public reference
implementation at https://github.com/CMGeldenhuys/Whale-VAD, unless otherwise
indicated in a comment.

This module is imported as ``cfg`` throughout the codebase; modifying a value
here propagates to every downstream component (data loading, model, training,
post-processing, inference).

Paper sections referenced in comments:
    - 5.1  Preprocessing (segmentation and windowing)
    - 5.2  Feature extractor (STFT, phase features, mean subtraction)
    - 5.3  Regularization (dropout)
    - 5.4  Whale-VAD model architecture
    - 5.5  Stochastic negative undersampling
    - 5.6  Loss function and optimizer
    - 5.8  Postprocessing (smoothing, thresholding, merging)

Reference
---------
Geldenhuys, C. M., Tonitz, D., & Niesler, T. (2025).
Whale-VAD: Whale Vocalisation Activity Detection.
In Proceedings of the Detection and Classification of Acoustic Scenes and
Events Challenge (DCASE 2025).
"""

